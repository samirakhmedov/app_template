/// Размеры кнопок в приложении.
enum AppButtonSize {
  /// Маленький размер.
  small,

  /// Средний размер.
  medium,

  /// Большой размер.
  large,
}

/// Состояния кнопки.
enum ButtonState {
  /// Активное состояние.
  active,

  /// Неактивное состояние.
  loading,

  /// Неактивное состояние.
  disabled,
}
